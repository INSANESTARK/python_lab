x=eval(input("enter any number"))
y=eval(input("enter any number"))
oper=input("enter any operator")
if(oper=='+'):
    add=x+y
    print("the sum is=",add)
if(oper=='-'):
    sub=x+y
    print("the subtraction is=",sub)
if(oper=='*'):
    mul=x+y
    print("the multiplication is=",mul)
if(oper=='/'):
    div=x+y
    print("the division is=",div)
    
